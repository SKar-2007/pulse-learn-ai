function makeTable(headers, rows, colWidths) {
  const total = colWidths.reduce((a, b) => a + b, 0);
  return new Table({
    width: { size: total, type: WidthType.DXA },
    columnWidths: colWidths,
    rows: [
      new TableRow({
        tableHeader: true,
        children: headers.map((h, i) => new TableCell({
          borders: BORDERS,
          width: { size: colWidths[i], type: WidthType.DXA },
          shading: { fill: '4338CA', type: ShadingType.CLEAR },
          margins: CELL_MARGIN,
          children: [new Paragraph({ children: [new TextRun({ text: h, bold: true, size: 18, font: 'Arial', color: 'FFFFFF' })] })],
        })),
      }),
      ...rows.map((row, ri) => new TableRow({
        children: row.map((cell, i) => new TableCell({
          borders: BORDERS,
          width: { size: colWidths[i], type: WidthType.DXA },
          shading: { fill: ri % 2 === 0 ? 'F5F3FF' : 'FFFFFF', type: ShadingType.CLEAR },
          margins: CELL_MARGIN,
          children: [new Paragraph({ children: [new TextRun({ text: cell, size: 18, font: 'Arial', color: '1F2937' })] })],
        })),
      })),
    ],
  });
}

function codeBlock(lines) {
  return [
    new Paragraph({
      spacing: { before: 80, after: 0 },
      shading: { fill: 'F1F5F9', type: ShadingType.CLEAR },
      indent: { left: 240, right: 240 },
      border: {
        top: { style: BorderStyle.SINGLE, size: 2, color: 'CBD5E1' },
        left: { style: BorderStyle.SINGLE, size: 2, color: 'CBD5E1' },
        right: { style: BorderStyle.SINGLE, size: 2, color: 'CBD5E1' },
        bottom: lines.length === 1 ? { style: BorderStyle.SINGLE, size: 2, color: 'CBD5E1' } : BorderStyle.NONE,
      },
      children: [new TextRun({ text: lines[0], size: 17, font: 'Courier New', color: '1E40AF' })],
    }),
    ...lines.slice(1).map((line, idx) => new Paragraph({
      spacing: { before: 0, after: idx === lines.length - 2 ? 80 : 0 },
      shading: { fill: 'F1F5F9', type: ShadingType.CLEAR },
      indent: { left: 240, right: 240 },
      border: {
        left: { style: BorderStyle.SINGLE, size: 2, color: 'CBD5E1' },
        right: { style: BorderStyle.SINGLE, size: 2, color: 'CBD5E1' },
        bottom: idx === lines.length - 2 ? { style: BorderStyle.SINGLE, size: 2, color: 'CBD5E1' } : undefined,
      },
      children: [new TextRun({ text: line, size: 17, font: 'Courier New', color: '1E40AF' })],
    })),
  ];
}

