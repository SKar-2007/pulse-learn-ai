    '      { headers: { Authorization: `Bearer ${accessToken}` } }',
    '    );',
    '    setComponents(prev => [...prev, data.component]);',
    '    return data.component;',
    '  };',
    '',
    '  const getShareLink = (component) =>',
    '    `${window.location.origin}/shared/component/${component.share_token}`;',
