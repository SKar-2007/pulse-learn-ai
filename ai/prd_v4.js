    'export function useLoopComponents(roadmapId, accessToken) {',
    '  const [components, setComponents] = useState([]);',
    '',
    '  useEffect(() => {',
    '    // Subscribe to real-time updates on loop_components',
    '    const channel = supabase',
    '      .channel(`loop-${roadmapId}`)',
    '      .on(\'postgres_changes\', {',
